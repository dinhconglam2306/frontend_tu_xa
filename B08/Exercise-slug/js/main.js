function changeToSlug(){

    let title = document.getElementById("title").value;
    let slug = title.toLowerCase();

    slug = slug.replace(/á|à|ả|ạ|ã|ă|ắ|ằ|ẳ|ẵ|ặ|â|ấ|ầ|ẩ|ẫ|ậ/mgi, 'a');
    slug = slug.replace(/é|è|ẻ|ẽ|ẹ|ê|ế|ề|ể|ễ|ệ/mgi, 'e');
    slug = slug.replace(/i|í|ì|ỉ|ĩ|ị/mgi, 'i');
    slug = slug.replace(/ó|ò|ỏ|õ|ọ|ô|ố|ồ|ổ|ỗ|ộ|ơ|ớ|ờ|ở|ỡ|ợ/mgi, 'o');
    slug = slug.replace(/ú|ù|ủ|ũ|ụ|ư|ứ|ừ|ử|ữ|ự/mgi, 'u');
    slug = slug.replace(/ý|ỳ|ỷ|ỹ|ỵ/mgi, 'y');
    slug = slug.replace(/đ/mgi, 'd');
    //Xóa các ký tự đặt biệt
    slug = slug.replace(/\`|\~|\!|\@|\#|\||\$|\%|\^|\&|\*|\(|\)|\+|\=|\,|\.|\/|\?|\>|\<|\'|\"|\:|\;|_/gi, '');
    //Đổi khoảng trắng thành ký tự gạch ngang
    slug = slug.replace(/ /mgi, "-");
    //Đổi nhiều ký tự gạch ngang liên tiếp thành 1 ký tự gạch ngang
    //Phòng trường hợp người nhập vào quá nhiều ký tự trắng
    slug = slug.replace(/\-\-\-\-\-/mgi, '-');
    slug = slug.replace(/\-\-\-\-/mgi, '-');
    slug = slug.replace(/\-\-\-/mgi, '-');
    slug = slug.replace(/\-\-/mgi, '-');
    //Xóa các ký tự gạch ngang ở đầu và cuối
    slug = '@' + slug + '@';
    slug = slug.replace(/\@\-|\-\@|\@/mgi, '');
    //In slug ra textbox có id “slug”
    document.getElementById('slug').value = slug;
}